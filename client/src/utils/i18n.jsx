const LANGS = ['en', 'ru', 'pl', 'de', 'fr'];

export function LanguageSwitcher() {
  const { lang, setLang } = useTranslation();
  return (
    <div className="lang-switcher">
      {LANGS.map(code => (
        <button
          key={code}
          className={'lang-btn' + (lang === code ? ' active' : '')}
          onClick={() => setLang(code)}
          type="button"
        >
          {code.toUpperCase()}
        </button>
      ))}
    </div>
  );
}
